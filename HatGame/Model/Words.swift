    enum level {
        case easy, medium, hard
    }
    
    func selectWords(from wordsLevel: Words.level, count: Int) {
        // Clear previous selection
        currentRoundSelectedWords.removeAll()

        // Determine the source set based on level
        let sourceSet: Set<String>
        switch wordsLevel {
        case .easy:
            sourceSet = easyWords
        case .medium:
            sourceSet = mediumWords
        case .hard:
            sourceSet = hardWords
        }

        var selection = Set<String>()
        let maxUnique = sourceSet.count

        // Start with words not used yet
        var available = sourceSet.subtracting(alreadyUsedWords).subtracting(selection.shuffled())

        // Pick items one by one until we reach the requested count
        while selection.count < count {
            // Prevent infinite loop if count exceeds unique words in this level
            if selection.count >= maxUnique { break }

            if available.isEmpty {
                // If we've exhausted all unused words, continue by allowing
                // previously used words, but never duplicate within this round.
                available = sourceSet.subtracting(selection)
            }

            guard let next = available.randomElement() else { break }
            available.remove(next)
            selection.insert(next)
            // Track as used across rounds (idempotent if it was already used)
            alreadyUsedWords.insert(next)
        }

        // Update state for the current round
        currentRoundSelectedWords = selection
        currentRoundLeftWords = selection
    }
    
    func getCurrentWord() -> String {
        if currentRoundLeftWords.isEmpty {
            return ""
        } else {
            return currentRoundLeftWords.randomElement() ?? ""
        }
    }
    
    func removeWordFrozmCurrentRoundLeftWords(currentWord: String) {
        currentRoundLeftWords.remove(currentWord)
    }
    
    func skipCurrentWord(currentWord: String) {
        currentTurnSkippedWords.insert(currentWord)
        currentRoundLeftWords.remove(currentWord)
    }
    
    func resetRound() {
        currentRoundLeftWords = currentRoundSelectedWords
    }
    
    func returningSkippedWords() {
        currentRoundLeftWords = currentRoundLeftWords.union(currentTurnSkippedWords)
        currentTurnSkippedWords.removeAll()
    }
